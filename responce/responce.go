func Responce(s string) string {
	var result string
	// –≤—Å–µ –ø—Ä–µ–¥–ª–æ–∂–µ–Ω–∏–µ –≤ –Ω–∏–∂–Ω–∏–π —Ä–µ–≥–∏—Å—Ç—Ä
	s = strings.ToLower(s)
	tkm := strings.Contains(s, "—Ç–∫")
	zavis := strings.Contains(s, "–∑–∞–≤–∏—Å")
	visit := strings.Contains(s, "–≤–∏—Å–∏—Ç")
	pk := strings.Contains(s, "–∫–æ–º–ø")
	igr := strings.Contains(s, "–∏–≥—Ä–æ–≤–æ–π")
	neR := strings.Contains(s, "–Ω–µ —Ä–µ–∞–≥–∏—Ä—É–µ—Ç")
	zayavka := strings.Contains(s, "–∑–∞—è–≤–∫–∞")
	program := strings.Contains(s, "—Å–æ—Ñ—Ç")

	if program {
		result = `–ò–º–µ–µ—Ç—Å—è 
		IVMS-4200,
		VMS tools, 
		KMS-Auto,
		KonBoot –¥–ª—è 7,
		–î—Ä–∞–π–≤–µ—Ä –Ω–∞ –ª–æ–∫–∞–ª–∫—É
		
		`
	}

	if tkm && zavis || visit && tkm || igr && zavis || igr && tkm {
		result = "–ü–æ—Å–º–æ—Ç—Ä–∏—Ç–µ –∏–Ω—Ç–µ—Ä–Ω–µ—Ç –∫–∞–±–µ–ª—è "
	}
	if pk && zavis || pk && visit {
		result = "–ü–µ—Ä–µ–∑–∞–≥—Ä—É–∑–∏—Ç–µ –∫–æ–º–ø"
	}
	if neR && tkm {
		result = "–ü—Ä–æ–≤–µ—Ä—å—Ç–µ –∫–∞–±–µ–ª—å –ø–∏—Ç–∞–Ω–∏—è, –µ—Å–ª–∏ —Ç–∞–∫ –∂–µ —Ç–æ –∑–∞–µ–¥—É —Å–∫–æ—Ä–æ –ø–æ—Å–º–æ—Ç—Ä—é"
	}
	if zayavka {
		result = "–•–æ—Ä–æ—à–æ –∑–∞–µ–¥—É"
	}

	return result
}

