func Video(chatID int64, name string, vName string) tgbotapi.VideoConfig {
	video := tgbotapi.NewVideo(chatID, tgbotapi.FilePath("Video/"+vName+".mp4"))
	video.Thumb = tgbotapi.FilePath("Video/" + vName + ".mp4")
	return video
}
